public class loop12 {
    public static void main(String[] args) {
        for(int i =0;i<=4;i++){
            for(int j=0;j<=i;j++){
                System.err.print("*");
            }for(int j =1;j<=2*(4-i);j++){
                System.err.print(" ");
            }for(int j=0;j<=i;j++){
                System.err.print("*");}
            System.err.println("  ");
        }for(int i=4;i>=0;i--){
            for(int j=0;j<=i;j++){
                System.err.print("*");
            }for(int j=1;j<=2*(4-i);j++){
                System.err.print(" ");
        }for(int j=0;j<=i;j++){
            System.err.print("*");}
            System.err.println("  ");
        
    }
    }
}
            
        
        
    




            
        
    

